import { mix } from '../src'

describe('mix()', () => {
  it('始点100から終点300の中間点を返す', () => {
    expect(mix(100, 300, 0.5)).toEqual(200)
  })

  it('第3引数に0未満の値を与えた場合に例外を投げる', () => {
    expect(() => mix(100, 300, -0.001)).toThrow()
  })

  it('第3引数に1より大きな値を与えた場合に例外を投げる', () => {
    expect(() => mix(100, 300, 1.001)).toThrow()
  })
})
